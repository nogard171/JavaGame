package classes;

public class Size {
	float Width = 0;
	float Height = 0;

	public float getWidth() {
		return this.Width;
	}

	public void setWidth(float width) {
		this.Width = width;
	}

	public float getHeight() {
		return this.Height;
	}

	public void setHeight(float height) {
		this.Height = height;
	}
}
